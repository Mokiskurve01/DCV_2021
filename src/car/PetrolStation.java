package car;

public class PetrolStation {

    private String name;
    private double dieselPricePerL;
    private double petrolPricePerL;

    public PetrolStation(String name, double dieselPricePerL, double petrolPricePerL) {
        this.name = name;
        this.dieselPricePerL = dieselPricePerL;
        this.petrolPricePerL = petrolPricePerL;
    }
}
